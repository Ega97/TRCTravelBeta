package com.example.trctravelbeta;

public class MobilModel {

    private String merk, plat, kursi, status;
    private int gambarMobil;

    public String getMerk() {
        return merk;
    }

    public void setMerk(String merk) {
        this.merk = merk;
    }

    public String getPlat() {
        return plat;
    }

    public void setPlat(String plat) {
        this.plat = plat;
    }

    public String getKursi() {
        return kursi;
    }

    public void setKursi(String kursi) {
        this.kursi = kursi;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getGambarMobil() {
        return gambarMobil;
    }

    public void setGambarMobil(int gambarMobil) {
        this.gambarMobil = gambarMobil;
    }
}
